-- VRP
-----------------------------------------------------------------------------------------------------------------------------------------
local Tunnel = module("vrp","lib/Tunnel")
local Proxy = module("vrp","lib/Proxy")
vRP = Proxy.getInterface("vRP")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
Humble = {}
Tunnel.bindInterface("digitalden",Humble)
Humble = Tunnel.getInterface("digitalden")
vSERVER = Tunnel.getInterface("digitalden")
-----------------------------------------------------------------------------------------------------------------------------------------
-- DIGITALDEN
-----------------------------------------------------------------------------------------------------------------------------------------
local digitaldenList = {
	-- DigitalDeen
	{ -1232.05,-1439.69,4.36,"Digital",false }
}
-----------------------------------------------------------------------------------------------------------------------------------------
-- THREADSTART
-----------------------------------------------------------------------------------------------------------------------------------------
CreateThread(function()
	SetNuiFocus(false,false)

	for k,v in pairs(digitaldenList) do
		exports["target"]:AddCircleZone("Digitalden:"..k,vec3(v[1],v[2],v[3]),1.75,{
			name = "Digitalden:"..k,
			heading = 3374176
		},{
			shop = k,
			Distance = 1.75,
			options = {
				{
					event = "digitalden:openDigitalden",
					label = "Abrir",
					tunnel = "shop"
				}
			}
		})
	end
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- DIGITALDEN:OPEN
-----------------------------------------------------------------------------------------------------------------------------------------
AddEventHandler("digitalden:openDigitalden", function(shopId)
        TriggerScreenblurFadeIn(500)
        SetNuiFocus(true, true)
        TriggerEvent("hud:Active", false)
        SendNUIMessage({ action = "show", items = Humble.getItems(), money = Humble.getMoney() })
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- CLOSE
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNUICallback("close",function(data)
	TriggerScreenblurFadeOut(250.0)
    Wait(250)
	SetNuiFocus(false,false)
    SetCursorLocation(0.5, 0.5)
	TriggerEvent("hud:Active", true)
	SendNUIMessage({ action = "hide" })
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- CALLBACK:COMPRAR
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNUICallback("comprar", function(data)
	if data and data.item and not block then
		setBlock()
		Humble.buyItem(data.item)
	end
end) 
-----------------------------------------------------------------------------------------------------------------------------------------
-- CALLBACK:BLOCK
-----------------------------------------------------------------------------------------------------------------------------------------
function setBlock()
	block = true
	Citizen.CreateThread(function()
		local time = 3
		while time > 0 do
			Wait(1000)
			time = time - 1
		end
		block = false
	end)
end